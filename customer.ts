export class Customer{

    custId:number;
    firstName:string;
    lastName:string;
    birthDate:Date;
    annualSalary:number;
    country:string;

    constructor(custId:number, firstName:string, lastName:string, birthDate:Date, annualSalary:number, country:string){
        
        this.custId=custId;
        this.firstName=firstName;
        this.lastName=lastName;
        this.birthDate=birthDate;
        this.annualSalary=annualSalary;
        this.country=country;

    }

}



























